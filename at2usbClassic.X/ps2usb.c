uint8_t PS2USB_ScanCodeToUSBHID(const PS2ScanCode *scanCode) {
    if (scanCode->isExtend) {
        return ps2ExtUsbMap[scanCode->value];
    } else {
        return ps2UsbMap[scanCode->value];
    }
}